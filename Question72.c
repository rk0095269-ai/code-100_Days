
// Find the sum of all elements in a matrix.

#include <stdio.h>
int main() {
    int r, c, i, j;
    int a[10][10];
    int total = 0;
    
    printf("Enter rows and columns: ");
    scanf("%d %d", &r, &c);
    
    printf("Enter matrix elements:\n");
    for(i = 0; i < r; i++) {
        for(j = 0; j < c; j++) {
            scanf("%d", &a[i][j]);
        }
    }
    
    // Find sum
    for(i = 0; i < r; i++) {
        for(j = 0; j < c; j++) {
            total += a[i][j];
        }
    }
    
    printf("Matrix:\n");
    for(i = 0; i < r; i++) {
        for(j = 0; j < c; j++) {
            printf("%d ", a[i][j]);
        }
        printf("\n");
    }
    
    printf("Total sum = %d\n", total);
    
    return 0;
}